"""
Tipos de Dados
str - string
int - inteiro
float - real/ponto flutuante
bool - boolean
"""
print('leo', type('leo'))
print('tipo', type(7))
print('tipo float', type(3.6))
print('true or false', type(True))

print('110', type('110'), type(int(110)))  # era string e convertemos pra int usando int()

# String : nome
print('leozin', type('leozin'))

# Idade : int
print(26, type(32))

# Altura : float
print(1.75, type(1.75))

# boolean true or false
print(18 > 17, type(18 > 17))