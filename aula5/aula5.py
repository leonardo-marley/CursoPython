# Operador de multiplicação também serve como repetição, ex.:
print('Olha o grito', 10 * 'A')

# Operador de adição também serve como concatenação, ex.:
print('Leonardo'+'Marley')

# O operador // faz divisão inteira somente(arredonda), ex.:
print(10 // 3)

# O operador ** faz potenciação, ex.:
print(2 ** 10)